package classe;

public class Operadores {

	double resultado;

	double soma(double a, double b) {
		return resultado = a + b;
	}

	double subtracao(double a, double b) {
		return resultado = a - b;
	}

	double multiplicacao(double a, double b) {
		return resultado = a * b;
	}

	double divisao(double a, double b) {
		return resultado = a / b;
	}
}
