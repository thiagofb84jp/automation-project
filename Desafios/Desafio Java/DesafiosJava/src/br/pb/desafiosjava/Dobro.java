package br.pb.desafiosjava;

public class Dobro {

	/*
	 * Escreva um programa que leia um número inteiro positivo e exiba o dobro do
	 * mesmo.
	 * 
	 */

	public static void main(String[] args) {
		int resultado;
		int num = 30;

		resultado = num * 2;

		System.out.println("Dobro do número " + num + " = " + resultado);
	}

}