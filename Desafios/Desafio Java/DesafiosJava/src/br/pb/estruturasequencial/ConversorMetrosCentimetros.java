package br.pb.estruturasequencial;

public class ConversorMetrosCentimetros {
	
	public static void main(String[] args) {
		double met = 8.9, cent;
		
		cent = met * 100;
		
		System.out.println("Conversor de metros para centímetros");
		System.out.println("O valor de " + met + " metros está agora a " 
		+ cent + " centímetros");
	}

}
